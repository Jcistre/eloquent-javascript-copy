function every(array, test) {
  if (array.length === 0) {
    return true
  }
  let yesSir = array.filter(function(yes) {
    if (test(yes) === true) {
      return yes
    }
  })
  if (yesSir.length === array.length) {
    return true;
  } else {
    return false
  }
  }

